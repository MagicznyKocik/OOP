package part2.compositioChallenge;

public class WindowDimensions {

    private int height;
    private int width;
    private int depth;

    public WindowDimensions(int height, int width, int depth) {
        this.height = height;
        this.width = width;
        this.depth = depth;
    }

    public int getHeight() {
        return height;
    }

    public int getWidth() {
        return width;
    }

    public int getDepth() {
        return depth;
    }


}
